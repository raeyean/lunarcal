/**
 * Look up a description for a festival or jieqi name.
 * Strips common prefixes/suffixes for fuzzy matching.
 */
export function getEventDescription(name: string): string | null {
  // Direct match first
  if (FESTIVAL_DESCRIPTIONS[name]) return FESTIVAL_DESCRIPTIONS[name];
  if (JIEQI_DESCRIPTIONS[name]) return JIEQI_DESCRIPTIONS[name];

  // Try matching jieqi from combined text like "清明將至 — 4月5日"
  for (const key of Object.keys(JIEQI_DESCRIPTIONS)) {
    if (name.includes(key)) return JIEQI_DESCRIPTIONS[key];
  }

  // Try matching festival from partial name
  for (const key of Object.keys(FESTIVAL_DESCRIPTIONS)) {
    if (name.includes(key) || key.includes(name)) return FESTIVAL_DESCRIPTIONS[key];
  }

  return null;
}

/**
 * Extract the primary event name from display text.
 * e.g. "清明將至 — 4月5日" -> "清明"
 */
export function extractEventName(text: string): string {
  // For jieqi text like "清明將至 — 4月5日" or just "清明"
  for (const key of Object.keys(JIEQI_DESCRIPTIONS)) {
    if (text.includes(key)) return key;
  }
  // For festival names, return as-is
  return text;
}
